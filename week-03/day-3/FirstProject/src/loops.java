public class loops {

    public static void main(String[] args) {


        // IWontCheatOnTheExam

        String line = "I won't cheat on the exam";

        for (int i = 0; i < 100; i++) {
            System.out.println(line);
        }


        // PrintEven

        int a = 0;
        while (a < 500) {
            System.out.println(a);
            a += 2;
        }

    }
}
